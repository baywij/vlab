<b>SAHAM TREASURY</b>
<p>
Saham treasury merupakan saham milik perusahaan penerbit yang pernah dikeluarkan 
dan beredar  kemudian dibeli kembali oleh perusahaan  tersebut.Alasan perusahaan 
membeli kembali sahamnya adalah :
<ul>
<li> Untuk memperkecil pajak</li>
<li> Untuk meningkatkan laba per saham dan pengembalian atas ekuitas</li>
<li> Mengurangi jumlah pemegang saham</li>
<li> Membentuk pasar bagi saham</li>
</br>
<p>Hal-hal yang perlu diingat dan diketahui:</p>
<li> Saham yang diperoleh kembali tidak boleh dianggap sebagai aktiva; malahan harus 
dilaporkan sebagai pengurang dari total ekuitas.</li>
<li>Tidak ada laba/rugi dari perolehan kembali, pelepasan kembali, atau penarikan saham 
yang diperoleh kembali</li>
<li>Laba ditahan dapat berkurang dari transaksi perolehan kembali saham tetapi tidak 
pernah bertambah karena transaksi ini.</li>
</p>
</ul>

