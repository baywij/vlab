<b>Deskripsi Modul</b></br>
&nbsp;&nbsp;&nbsp;&nbsp;
Di dalam perusahaan, kebijakan deviden merupakan bagian penting dalam 
pengambilan keputusan pendanaan dimana laba yang diperoleh perusahaan pada akhir tahun 
dibagikan kepada para pemegang saham dalam bentuk deviden atau ditahan untuk menambah 
modal yang digunakan sebagai pembiayaan investasi dimasa yang akan datang. Jika laba 
ditahan perusahaan saat ini dalam jumlah besar, maka laba yang akan dibayarkan sebagai 
deviden kepada para pemegang saham menjadi lebih kecil. 
</br>
</br>
&nbsp;&nbsp;&nbsp;&nbsp;
Dalam setiap periode, perusahaan harus memutuskan apakah laba yang diperoleh 
akan ditahan atau didirtribusikan sebagian atau seluruhnya pada pemegang saham. Selama 
perusahaan memiliki proyek investasi dengan pengembalian melebihi yang diminta, 
perusahaan akan menggunakan laba untuk mendanai proyek tersebut. Jika terdapat kelebihan 
laba setelah digunakan untuk mendanai seluruh kesempatan investasi yang diterima, 
kelebihan itu akan didistribusikan kepada pemegang saham dalam bentuk dividen kas. Jika 
tidak ada kelebihan, maka dividen tidak akan di bagikan.
</br>
</br>
<b>PENGERTIAN</b>
</br>
&nbsp;&nbsp;&nbsp;&nbsp;
Salah satu kebijakan yang harus diambil oleh manajemen pada masa setelah tutup 
buku adalah laba yang diperoleh oleh perusahaan selama satu periode akan dibagi 
sebagian untuk deviden dan sebagian lagi dibagi dalam laba ditahan. 
</br>
</br>
&nbsp;&nbsp;&nbsp;&nbsp;
<b><i>Kebijakan Deviden</i></b> adalah kebijakan untuk menentukan berapa laba yang harus 
dibayarkan (deviden) kepada pemegang saham dan berapa banyak yang harus ditanam 
kembali (laba ditahan) untuk menambah modal guna pembiayaan investasi dimasa yang 
akan datang.
</br>
</br>
&nbsp;&nbsp;&nbsp;&nbsp;
<b><i>Deviden</i></b> adalah pendapatan bagi pemegang saham yang dibayarkan setiap akhir 
periode sesuai dengan persentasenya. Persentase dari laba yang akan dibagikan sebagai 
deviden kepada pemegang saham disebut sebagai <b><i>Deviden Payout Ratio </i></b>(perbandingan 
antara DPS dengan EPS).
</br>
</br>
&nbsp;&nbsp;&nbsp;&nbsp;
Bagi para investor, dividen merupakan hasil yang diperoleh dari saham yang dimiliki, 
selain <i>capital gain</i> yang didapat apabila harga jual saham lebih tinggi dibanding harga 
belinya. Dividen tersebut didapat dari perusahaan sebagai distribusi yang  dihasilkan dari 
operasi perusahaan.
</br>
</br>
&nbsp;&nbsp;&nbsp;&nbsp;
Apabila perusahaan memilih untuk membagikan laba sebagai dividen, maka akan 
mengurangi laba yang ditahan dan selanjutnya mengurangi total sumber dana interen atau 
<i>internal financing.</i> Sebaliknya jika perusahaan memilih untuk menahan laba yang 
diperoleh, maka kemampuan pembentukan dana interen akan semakin besar.
</br>
</br>

<b>FAKTOR YANG MEMPENGARUHI KEBIJAKAN DEVIDEN</b>
</br>
&nbsp;&nbsp;&nbsp;&nbsp;
Adapun faktor-faktor yang mempengaruhi besar kecilnya deviden yang dibayarkan 
oleh perusahaan kepada pemegang saham antara lain :
<ul>
<li>Posisi Likuiditas Perusahaan</li>
<li>Kebutuhan Dana Untuk Membayar Hutang</li>
<li>Tingkat Pertumbuhan Perusahaan</li>
<li>Pengawasan Terhadap Perusahaan</li>
<li>Kemampuan Meminjam</li>
<li>Tingkat Keuntungan</li>
<li>Stabilitas Return</li>
<li>Akses Ke pasar Modal</li>
</ul>
</br>
<b>MACAM-MACAM KEBIJAKAN DEVIDEN</b>
</br>
<ol>
<li><b>Kebijakan dividen yang stabil</b></li>
Banyak perusahaan yang menjalankan kebijakan dividen yang stabil, artinya jumlah 
dividen per lembar yang dibayarkan setiap tahunnya relatif tetap selama jangka waktu 
tertentu meskipun pendapatan per lembar saham setiap tahunnya berfluktuasi. 
</br>
<li><b>Kebijakan dividen dengan penetapan jumlah dividen minimal plus jumlah ekstra
tertentu</b></li>
Kebijakan ini menetapkan jumlah rupiah minimal dividen per lembar saham tiap 
tahunnya. Dalam keadaan keuangan yang lebih baik perusahaan akan membayarkan 
dividen ekstra diatas jumlah minimal tersebut.
</br>
<li><b>Kebijakan dividen dengan penetapan dividend payout ratio yang konstan</b></li>
Jenis kebijakan dividen yang ketiga adalah penetapan dividend payout ratioyang 
konstan. Perusahaan yang menjalankan kebijakan ini menetapkan dividen payout 
ratio yang konstan misalnya 50%. Ini berarti bahwa jumlah dividen per lembar saham 
yang dibayarkan setiap tahunnya akan berfluktuasi sesuai dengan perkembangan 
keuntungan netto yang diperoleh setiap tahunnya.
<li><b>Kebijakan dividen yang fleksibel</b></li>
Kebijakan dividen yang terakhir adalah penetapan dividen payout ratio yang fleksibel, 
yang besarnya setiap tahun disesuaikan dengan posisi financial dan 
kebijakan financial dari perusahaan yang bersangkutan.
</br>
</ol>
