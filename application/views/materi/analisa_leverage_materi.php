<b>Deskripsi Modul</b></br>
<p>&nbsp;&nbsp;&nbsp;&nbsp;
Analisis financial leverage digunakan untuk mengukur sampai berapa jauh 
perusahaan telah dibiayai dengan utang-utang jangka panjang dan untuk mengukur tingkat 
kepekaan antara EPS terhadap perubahan EBIT perusahaan.  Leverage  adalah penggunaan 
dana yang diperoleh dari utang atau dengan mengeluarkan saham preferen, biaya tetap yang 
ditimbulkan dari penggunaan dana tersebut adalah bunga atau deviden. Bunga dan deviden 
saham preferen merupakan biaya tetap finansial yang harus dibayar tanpa mempedulikan 
tingkat laba perusahaan. Sehingga  financial leverage  dikatakan sebagai pengungkit 
keuangan, dimana perusahaan membiayai kegiatannya (operasional) dengan menggunakan 
modal pinjaman serta menanggung suatu beban tetap yang bertujuan untuk meningkatkan 
laba per lembar saham.</p>
</br>
<b>Pengertian <i>Financial Leverage</i></b>
<p>&nbsp;&nbsp;&nbsp;&nbsp;
<i>Financial leverage</i> adalah penggunaan sumber dana yang memiliki beban tetap 
dengan harapan bahwa akan memberikan tambahan keuntungan yang lebih besar daripada 
beban tetapnya sehingga akan meningkatkan keuntungan yang tersedia bagi pemegang 
saham. Financial leverage menunjukkan perubahan laba per lembar saham atau <i>earning 
per share</i> (EPS) sebagai akibat perubahan <i>earning before interest and tax</i> (EBIT).
<b><i>Obligasi</i></b> adalah surat utang jangka panjang yang diterbitkan oleh pemerintah atau perusahaan.	
<p><b><i>&nbsp;&nbsp;&nbsp;&nbsp;Saham biasa</i></b> 
adalah saham yang pembayarannya tidak pasti, dimana jumlah dan devidennya tidak tetap, dan pemilik saham biasa memiliki hak suara dalam RUPS. </p>
<p><b><i>&nbsp;&nbsp;Saham preferen</i></b>
adalah saham yang pembayarannya dilakukan terlebih dahulu dalam pembagian deviden, jumlahnya tetap dan telah dinyatakan sebelumnya. Namun pemilik saham preferen tidak memiliki hak suara dalam RUPS.
<p>&nbsp;&nbsp;&nbsp;&nbsp;Untuk dapat menentukan income effect dari berbagai pertimbangan (alternative) perlu 
diketahui tingkat EBIT yang dapat menghasilkan EPS yang sama besar.  Tingkat EBIT 
yang menghasilkan EPS yang sama besarnya pada berbagai perimbangan pembelanjaan
<i>(financial mix)</i> dinamakan <i>Indifference Point</i> atau <i>Break Event Point</i> dalam <i>financial leverage.</i></p>
<b><i>Indifference Point</i> antara hutang dengan saham biasa</b>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;Untuk dapat mengetahui perimbangan pembelanjaan yang mana yang mempunyai <i>income effect</i>  yang terbesar terhadap EPS pada setiap tingkat EBIT, maka kita harus menentukan  <i>indifference point</i> terlebih dahulu. Setelah menentukan <i>indifference point</i> maka dapat diketahui  pada tingkat EBIT berapa akan menghasilkan EPS pada berbagai alternatif adalah sama. Analisa <i>Indifference point</i> ini sering pula disebut dengan “analisa EBIT – EPS”.</p>
